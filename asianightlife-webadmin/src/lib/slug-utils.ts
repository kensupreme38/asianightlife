const VIET_CHAR_MAP: Record<string, string> = {
  à: "a", á: "a", ả: "a", ã: "a", ạ: "a",
  ă: "a", ằ: "a", ắ: "a", ẳ: "a", ẵ: "a", ặ: "a",
  â: "a", ầ: "a", ấ: "a", ẩ: "a", ẫ: "a", ậ: "a",
  è: "e", é: "e", ẻ: "e", ẽ: "e", ẹ: "e",
  ê: "e", ề: "e", ế: "e", ể: "e", ễ: "e", ệ: "e",
  ì: "i", í: "i", ỉ: "i", ĩ: "i", ị: "i",
  ò: "o", ó: "o", ỏ: "o", õ: "o", ọ: "o",
  ô: "o", ồ: "o", ố: "o", ổ: "o", ỗ: "o", ộ: "o",
  ơ: "o", ờ: "o", ớ: "o", ở: "o", ỡ: "o", ợ: "o",
  ù: "u", ú: "u", ủ: "u", ũ: "u", ụ: "u",
  ư: "u", ừ: "u", ứ: "u", ử: "u", ữ: "u", ự: "u",
  ỳ: "y", ý: "y", ỷ: "y", ỹ: "y", ỵ: "y",
  đ: "d",
};

function transliterateToAscii(text: string): string {
  const mapped = text
    .split("")
    .map((char) => VIET_CHAR_MAP[char] ?? VIET_CHAR_MAP[char.toLowerCase()] ?? char)
    .join("");

  return mapped.normalize("NFD").replace(/[\u0300-\u036f]/g, "");
}

function finalizeSlug(text: string): string {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9\s-]/g, "")
    .replace(/\s+/g, "-")
    .replace(/-+/g, "-")
    .trim()
    .replace(/^-+|-+$/g, "");
}

export function generateSlug(name: string): string {
  return finalizeSlug(transliterateToAscii(name));
}

export function getVenueSlug(venue: { slug?: string | null; name: string }): string {
  const fresh = generateSlug(venue.name);
  const stored = venue.slug?.trim();
  if (!stored) return fresh;
  const legacy = finalizeSlug(
    venue.name
      .toLowerCase()
      .replace(/[^\w\s-]/g, "")
      .replace(/\s+/g, "-")
      .replace(/-+/g, "-")
      .trim()
      .replace(/^-+|-+$/g, "")
  );
  if (stored === legacy) return fresh;
  return stored;
}
